    private void cmdSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdSubmitActionPerformed
        String tampilan = "yyyy-MM-dd";
        SimpleDateFormat format = new SimpleDateFormat(tampilan);
        RumahSakitApp rsa = new RumahSakitApp();
        
        String nomedrec = tb_nomedrec.getText();
        String nama = tb_nama.getText();
        String tempat_lahir = tb_tempat.getText();
        String tgl_lahir = String.valueOf(format.format(tb_tanggal.getDate()));
        String alamat = tb_alamat.getText();
        String kota = tb_kota.getText();
        String telp = tb_telp.getText();

        if(action.equals("add")){
            try{
                Statement stmt = koneksi.createStatement();
                String query = "INSERT INTO pasien(nomedrec, nama_pasien, tempat_lahir, tgl_lahir, alamat, "
                + "kota, telepon)"
                + "VALUE('"+nomedrec+"','"+nama+"','"+tempat_lahir+"','"
                + tgl_lahir+"','"+alamat+"','"+kota+"','"
                + telp+"')";
                int berhasil = stmt.executeUpdate(query);
                if(berhasil == 1){
                    JOptionPane.showMessageDialog(null, "Data Berhasil Ditambahkan!");
                } else {
                    JOptionPane.showMessageDialog(null, "Data Gagal Ditambahkan!");
                }
            } catch (SQLException ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(null, "Terjadi Kesalahan di Query!");
            }
            setVisible(false);
            rsa.showDataPasien();
        } else {
            try{
                Statement stmt = koneksi.createStatement();
                String query = "UPDATE pasien SET nama_pasien = '"+nama
                +"', tempat_lahir = '"+tempat_lahir+"', tgl_lahir = '"+tgl_lahir+"', "
                + "alamat = '"+alamat+"', kota = '"+kota+"', telepon = '"+telp+"' WHERE nomedrec = '"+idpasien+"'";
                int berhasil = stmt.executeUpdate(query);
                if(berhasil == 1){
                    JOptionPane.showMessageDialog(null, "Data Berhasil Diubah!");
                } else {
                    JOptionPane.showMessageDialog(null, "Data Gagal Diubah!");
                }
            } catch (SQLException ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(null, "Terjadi Kesalahan di Query!");
            }
            setVisible(false);
            rsa.showDataPasien();
        }
    }//GEN-LAST:event_cmdSubmitActionPerformed

