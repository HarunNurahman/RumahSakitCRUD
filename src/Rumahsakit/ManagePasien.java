    void ShowForm(String act, String nomedrec){
        if (act.equals("edit")){
            try{
                Statement stmt = koneksi.createStatement();
                String query = "SELECT * FROM pasien WHERE nomedrec = '"+nomedrec+"'";
                ResultSet rs = stmt.executeQuery(query);
                rs.first();
                tb_nomedrec.setText(rs.getString("nomedrec"));
                tb_nama.setText(rs.getString("nama_pasien"));
                tb_tempat.setText(rs.getString("tempat_lahir"));
                tb_tanggal.setDate(rs.getDate("tgl_lahir"));
                tb_alamat.setText(rs.getString("alamat"));
                tb_kota.setText(rs.getString("kota"));
                tb_telp.setText(rs.getString("telepon"));
            } catch (SQLException ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(null, "Terjadi Kesalahan di Query!");
            }
        }
    }
